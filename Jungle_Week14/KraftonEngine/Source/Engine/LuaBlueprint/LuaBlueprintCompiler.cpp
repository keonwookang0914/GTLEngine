        void EmitSequence(
            std::ostringstream&         Out,
            const FLuaBlueprintNode&    Node,
            int                         Indent,
            std::unordered_set<uint32>& ExecStack
            )
        {
            TArray<const FLuaBlueprintPin*> Outputs;
            for (const FLuaBlueprintPin& Pin : Node.Pins)
            {
                if (Pin.Kind == ELuaBlueprintPinKind::Output && Pin.Type == ELuaBlueprintPinType::Exec)
                {
                    Outputs.push_back(&Pin);
                }
            }
            std::sort(
                Outputs.begin(),
                Outputs.end(),
                [](const FLuaBlueprintPin* A, const FLuaBlueprintPin* B)
                {
                    return A->DisplayName.ToString() < B->DisplayName.ToString();
                }
            );
            for (const FLuaBlueprintPin* Pin : Outputs)
            {
                EmitNextExecFromPin(Out, Pin->PinId, Indent, ExecStack);
            }
        }

