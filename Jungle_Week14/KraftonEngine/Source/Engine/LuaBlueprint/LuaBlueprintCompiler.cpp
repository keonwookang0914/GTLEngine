        void EmitDelay(
            std::ostringstream&         Out,
            const FLuaBlueprintNode&    Node,
            int                         Indent,
            std::unordered_set<uint32>& ExecStack
            )
        {
            const FString Seconds = GetInputExpression(Node, "Seconds", "0");
            EmitIndent(Out, Indent);
            Out << "if BP_Delay then\n";
            EmitIndent(Out, Indent + 1);
            Out << "BP_Delay(" << Seconds << ", function()\n";
            EmitThen(Out, Node, Indent + 2, ExecStack);
            EmitIndent(Out, Indent + 1);
            Out << "end)\n";
            EmitIndent(Out, Indent);
            Out << "else\n";
            EmitIndent(Out, Indent + 1);
            Out << "-- No component-owned delay scheduler is available; skip unsafe global coroutine fallback.\n";
            EmitIndent(Out, Indent);
            Out << "end\n";
        }

        // Cast: Success/Failed exec 분기. Result 데이터 output 은 Success 경로에서만 유효.
        void EmitCast(
            std::ostringstream&         Out,
            const FLuaBlueprintNode&    Node,
            int                         Indent,
            std::unordered_set<uint32>& ExecStack
            )
        {
            const FString ClassName = LuaName(Node.NameValue);
            if (ClassName.empty())
            {
                AddDiagnostic(Result, ELuaBlueprintDiagnosticSeverity::Error, Node.NodeId, "Cast has no class name");
                return;
            }
            const FString Local = FString("__c") + std::to_string(Node.NodeId);
            EmitIndent(Out, Indent);
            Out << "local " << Local << " = Reflection.Cast(" << GetInputExpression(Node, "Target", "nil") << ", " <<
            LuaQuoted(ClassName) << ")\n";

            EmitIndent(Out, Indent);
            Out << "if " << Local << " ~= nil then\n";
            ExecLocals[Node.NodeId] = Local;
            if (const FLuaBlueprintPin* Success = FindPinByName(Node, "Success", ELuaBlueprintPinKind::Output))
            {
                EmitNextExecFromPin(Out, Success->PinId, Indent + 1, ExecStack);
            }
            ExecLocals.erase(Node.NodeId);
            EmitIndent(Out, Indent);
            Out << "else\n";
            if (const FLuaBlueprintPin* Failed = FindPinByName(Node, "Failed", ELuaBlueprintPinKind::Output))
            {
                EmitNextExecFromPin(Out, Failed->PinId, Indent + 1, ExecStack);
            }
            EmitIndent(Out, Indent);
            Out << "end\n";
        }

        // ForEachActor — class 또는 tag 기반. iteration 동안 Actor pin/Index pin 매핑 set.
        void EmitForEachActor(
            std::ostringstream&         Out,
            const FLuaBlueprintNode&    Node,
            int                         Indent,
            std::unordered_set<uint32>& ExecStack,
            bool                        bByClass
            )
        {
            const FString Key = bByClass ? LuaName(Node.NameValue) : Node.StringValue;
            if (Key.empty())
            {
                AddDiagnostic(
                    Result,
                    ELuaBlueprintDiagnosticSeverity::Error,
                    Node.NodeId,
                    bByClass ? "ForEachActor has no class name" : "ForEachActor has no tag"
                );
                return;
            }
            const FString ActorLocal = FString("__a") + std::to_string(Node.NodeId);
            const FString IndexLocal = FString("__i") + std::to_string(Node.NodeId);
            const char*   Source     = bByClass ? "World.FindActorsByClass" : "World.FindActorsByTag";

            EmitIndent(Out, Indent);
            Out << "for " << IndexLocal << ", " << ActorLocal << " in ipairs(" << Source << "(" << LuaQuoted(Key) <<
            ")) do\n";
            ForEachActorNodeIds[Node.NodeId] = ActorLocal;
            LoopIndexNodeIds[Node.NodeId]    = IndexLocal;
            if (const FLuaBlueprintPin* Loop = FindPinByName(Node, "Loop", ELuaBlueprintPinKind::Output))
            {
                EmitNextExecFromPin(Out, Loop->PinId, Indent + 1, ExecStack);
            }
            ForEachActorNodeIds.erase(Node.NodeId);
            LoopIndexNodeIds.erase(Node.NodeId);
            EmitIndent(Out, Indent);
            Out << "end\n";
            if (const FLuaBlueprintPin* Done = FindPinByName(Node, "Completed", ELuaBlueprintPinKind::Output))
            {
                EmitNextExecFromPin(Out, Done->PinId, Indent, ExecStack);
            }
        }

