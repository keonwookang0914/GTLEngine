void AActor::EndPlay()
{
	if (!bActorHasBegunPlay) return;
	bActorHasBegunPlay = false;
	PrimaryActorTick.UnRegisterTickFunction();

	for (UActorComponent* Comp : OwnedComponents)
	{
		if (Comp)
		{
			Comp->PrimaryComponentTick.UnRegisterTickFunction();
			Comp->EndPlay();
		}
	}
}

